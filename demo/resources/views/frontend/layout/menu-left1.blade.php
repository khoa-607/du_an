<script>
    $(document).ready(function() {
        $('#sl2').slider({ //trỏ vào phần tử có id s12 và thực hiện hành động trượt
            formatter: function(value) {
                return '$' + value; // lấy giá trị của phần tử có id s12, đặt $ trước giá trị 
            }
        }).on('slideStop', function(event) { //dừng hành động trượt, event chứa thông tin về khoảng giá trị sau khi dừng hành động
            var range = event.value; 
            var priceRange = range[0] + ':' + range[1]; // lấy giá trị sau khi dừng hành động trượt và gửi đi dưới dạng chuỗi 

            $.ajax({
                url: "{{ route('member-home.filter-products') }}",
                method: "GET",
                data: { price_range: priceRange },
                success: function(response) {
                    $('#products').html(response);
                }
            });
        });
    });
</script>